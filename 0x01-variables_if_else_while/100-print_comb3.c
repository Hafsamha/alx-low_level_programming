#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
	int n, x;
	{

	for (n = '0'; n < '9'; n++)
		putchar('n');
	
	{
	for (x = n + 1; x <= '9'; x++)
			putchar('x');
	
	{
		if (x != n)
			if (n == '8' && x == '9')
				continue;
			putchar(',');
			putchar(' ');
	}
	}
	}
	
			putchar('\n');
			return (0);
}
